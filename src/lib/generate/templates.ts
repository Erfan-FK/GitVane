// Orchestrate all template files
// ---------------------------------------------------------------------------
export function buildAllFiles(profile: RepoProfile, prose: ProseBlocks): GeneratedFile[] {
  const enriched: GeneratedFile["source"] =
    Object.keys(prose).length > 0 ? "llm-verified" : "template";

  const files: GeneratedFile[] = [
    { path: "AGENTS.md", content: buildAgentsMd(profile, prose), language: "markdown", source: enriched, tier: "free" },
    { path: "llms.txt", content: buildLlmsTxt(profile, prose), language: "markdown", source: enriched, tier: "free" },
    { path: "CLAUDE.md", content: buildClaudeMd(profile), language: "markdown", source: "template", tier: "free" },
    {
      path: ".github/copilot-instructions.md",
      content: buildCopilotInstructions(profile, prose),
      language: "markdown",
      source: enriched,
      tier: "free",
    },
    {
      path: ".cursor/rules/project.mdc",
      content: buildCursorRules(profile, prose),
      language: "markdown",
      source: enriched,
      tier: "free",
    },
    {
      path: ".windsurf/rules/project.md",
      content: buildWindsurfRules(profile, prose),
      language: "markdown",
      source: enriched,
      tier: "free",
    },
    {
      path: "docs/validation.md",
      content: buildValidationMd(profile),
      language: "markdown",
      source: "template",
      tier: "free",
    },
    {
      path: "docs/failure-modes.md",
      content: buildFailureModesMd(profile, prose),
      language: "markdown",
      source: "template",
      tier: "free",
    },
  ];

  if (profile.hasFrontend) {
    files.push({
      path: "docs/design-system.md",
      content: buildDesignSystemMd(profile, prose),
      language: "markdown",
      source: prose.designNotes ? "llm-verified" : "template",
      tier: "premium",
    });
  }
  if (profile.hasBackend) {
    files.push({
      path: "docs/api-map.md",
      content: buildApiMapMd(profile, prose),
      language: "markdown",
      source: prose.apiNotes ? "llm-verified" : "template",
      tier: "premium",
    });
  }

  // Path-scoped Copilot instructions.
  if (profile.hasFrontend) {
    files.push({
      path: ".github/instructions/frontend.instructions.md",
      content: buildFrontendInstructions(profile),
      language: "markdown",
      source: "template",
      tier: "premium",
    });
  }
  if (profile.hasBackend) {
    files.push({
      path: ".github/instructions/backend.instructions.md",
      content: buildBackendInstructions(profile),
      language: "markdown",
      source: "template",
      tier: "premium",
    });
  }

  // Auth flow when authentication signals are detected.
  if (hasAuthSignals(profile)) {
    files.push({
      path: "docs/auth-flow.md",
      content: buildAuthFlowMd(profile),
      language: "markdown",
      source: "template",
      tier: "premium",
    });
  }

  // Database guide when a data layer is detected.
  if (profile.database.length) {
    files.push({
      path: "docs/database.md",
      content: buildDatabaseMd(profile),
      language: "markdown",
      source: "template",
      tier: "premium",
    });
  }

  // Environment variables when any are detected.
  if (profile.envVars.length) {
    files.push({
      path: "docs/env-vars.md",
      content: buildEnvVarsMd(profile),
      language: "markdown",
      source: "template",
      tier: "premium",
    });
  }

  return files;
}
