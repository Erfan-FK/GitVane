// .github/instructions/frontend.instructions.md (path-scoped Copilot rules)
// ---------------------------------------------------------------------------
export function buildFrontendInstructions(profile: RepoProfile): string {
  const styleBullets: string[] = [];
  if (profile.styling.includes("Tailwind CSS"))
    styleBullets.push("Use Tailwind utility classes and existing theme tokens; avoid one-off inline styles or arbitrary colors.");
  if (profile.uiLibraries.includes("shadcn/ui"))
    styleBullets.push("Compose existing shadcn/ui primitives instead of restyling or hand-rolling them.");
  if (profile.uiLibraries.length)
    styleBullets.push(`Reuse the project's UI libraries (${list(profile.uiLibraries)}) rather than adding new ones.`);
  styleBullets.push("Reuse existing components, and existing loading / empty / error states.");
  styleBullets.push("Keep spacing, radius, and typography consistent with current components.");
  if (profile.hasBackend)
    styleBullets.push("Never import server-only modules into client components.");

  return `---
applyTo: "${applyToGlob(profile.frontendDirs, "src/**")}"
---

# Frontend instructions

Path-scoped guidance for UI code in ${profile.owner}/${profile.repo}. These rules apply only to the frontend directories above.

## Stack
- ${stackSummary(profile)}
- Styling: ${list(profile.styling, "not detected")}
${profile.uiLibraries.length ? `- UI libraries: ${list(profile.uiLibraries)}` : ""}
${profile.frontendDirs.length ? `- Frontend lives in: ${profile.frontendDirs.map((d) => `\`${d}/\``).join(", ")}` : ""}

## Rules
${bullets(styleBullets)}

See \`docs/design-system.md\` for the full design system.
`;
}

