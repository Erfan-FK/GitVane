import type { RepoProfile, GeneratedFile } from "@/lib/types";
import type { ProseBlocks } from "@/lib/generate/prose";

function list(items: string[], fallback = "—"): string {
  return items.length ? items.join(", ") : fallback;
}

function bullets(items: string[]): string {
  return items.map((i) => `- ${i}`).join("\n");
}

function commandsBlock(profile: RepoProfile): string {
  const s = profile.scripts;
  const lines: string[] = [];
  if (s.install) lines.push(s.install);
  if (s.dev) lines.push(s.dev);
  if (s.lint) lines.push(s.lint);
  if (s.typecheck) lines.push(s.typecheck);
  if (s.test) lines.push(s.test);
  if (s.build) lines.push(s.build);
  if (lines.length === 0) lines.push("# No package.json scripts detected");
  return lines.join("\n");
}

function stackSummary(profile: RepoProfile): string {
  const parts: string[] = [];
  if (profile.frameworks.length) parts.push(profile.frameworks.join(" + "));
  if (profile.languages.length) parts.push(profile.languages.slice(0, 3).join("/"));
  if (profile.packageManager !== "unknown") parts.push(`${profile.packageManager}`);
  return parts.join(" · ") || "Unknown stack";
}

function defaultSummary(profile: RepoProfile): string {
  if (profile.description) return profile.description;
  const fw = profile.frameworks[0] ?? profile.primaryLanguage ?? "software";
  return `${profile.repo} is a ${fw} project.`;
}

function defaultArchitecture(profile: RepoProfile): string {
  const segs: string[] = [];
  if (profile.frameworks.length) {
    segs.push(`Built with ${list(profile.frameworks)}.`);
  }
  if (profile.hasFrontend && profile.frontendDirs.length) {
    segs.push(`Frontend code lives in ${list(profile.frontendDirs)}.`);
  }
  if (profile.hasBackend && profile.backendDirs.length) {
    segs.push(`Server/API code lives in ${list(profile.backendDirs)}.`);
  }
  if (profile.database.length) {
    segs.push(`Data layer uses ${list(profile.database)}.`);
  }
  if (profile.monorepo) {
    segs.push("This is a monorepo with multiple workspaces.");
  }
  return segs.join(" ") || "See the directory layout for how the project is organized.";
}

function defaultConventions(profile: RepoProfile): string[] {
  const c: string[] = [];
  if (profile.packageManager !== "unknown") {
    c.push(`Use \`${profile.packageManager}\` as the package manager.`);
  }
  if (profile.languages.includes("TypeScript")) {
    c.push("Write TypeScript with explicit types for public APIs; avoid `any`.");
  }
  if (profile.styling.includes("Tailwind CSS")) {
    c.push("Style with Tailwind utility classes; avoid ad-hoc inline styles.");
  }
  if (profile.uiLibraries.includes("shadcn/ui")) {
    c.push("Reuse existing shadcn/ui components instead of creating new primitives.");
  }
  if (profile.testing.length) {
    c.push(`Add/update tests using ${list(profile.testing)} when changing behavior.`);
  }
  c.push("Match the existing file structure and naming conventions.");
  c.push("Keep changes focused; do not reformat unrelated code.");
  return c;
}

function directoryBlock(profile: RepoProfile): string {
  if (!profile.notableDirs.length) return "";
  return profile.notableDirs.map((d) => `- \`${d}/\``).join("\n");
}

