// llms.txt (root LLM index — see llmstxt.org)
// ---------------------------------------------------------------------------
export function buildLlmsTxt(profile: RepoProfile, prose: ProseBlocks): string {
  const summary = prose.summary || defaultSummary(profile);
  const repoUrl = `https://github.com/${profile.owner}/${profile.repo}`;

  const docLinks: string[] = [
    `- [AGENTS.md](${repoUrl}/blob/${profile.defaultBranch}/AGENTS.md): Operating instructions for AI coding agents.`,
    `- [Validation runbook](${repoUrl}/blob/${profile.defaultBranch}/docs/validation.md): Exact install, lint, test, and build commands.`,
    `- [Failure modes](${repoUrl}/blob/${profile.defaultBranch}/docs/failure-modes.md): Risky areas and files not to touch.`,
  ];
  if (profile.hasFrontend) {
    docLinks.push(
      `- [Design system](${repoUrl}/blob/${profile.defaultBranch}/docs/design-system.md): UI and styling conventions.`,
    );
  }
  if (profile.hasBackend) {
    docLinks.push(
      `- [API map](${repoUrl}/blob/${profile.defaultBranch}/docs/api-map.md): Routes, handlers, and server conventions.`,
    );
  }

  const dirs = profile.notableDirs.length
    ? profile.notableDirs.map((d) => `- \`${d}/\``).join("\n")
    : "- See the repository root for layout.";

  return `# ${profile.owner}/${profile.repo}

> ${summary}

- **Stack:** ${stackSummary(profile)}
- **Primary language:** ${profile.primaryLanguage ?? list(profile.languages)}
- **Package manager:** ${profile.packageManager}
- **Repository:** ${repoUrl}

## Docs

${docLinks.join("\n")}

## Key directories

${dirs}

## Commands

\`\`\`bash
${commandsBlock(profile)}
\`\`\`

## Notes

This file is an [llms.txt](https://llmstxt.org) index generated by GitVane. Read \`AGENTS.md\` for the full operating instructions before making changes.
`;
}

