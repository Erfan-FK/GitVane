// ---------------------------------------------------------------------------
// docs/validation.md
// ---------------------------------------------------------------------------
export function buildValidationMd(profile: RepoProfile): string {
  const s = profile.scripts;
  const rows: Array<[string, string | null]> = [
    ["Install", s.install],
    ["Dev server", s.dev],
    ["Lint", s.lint],
    ["Typecheck", s.typecheck],
    ["Test", s.test],
    ["Build", s.build],
    ["Format", s.format],
    ["Start", s.start],
  ];
  const table = rows
    .map(([label, cmd]) => `| ${label} | ${cmd ? `\`${cmd}\`` : "_not detected_"} |`)
    .join("\n");

  const ordered = [s.install, s.lint, s.typecheck, s.test, s.build].filter(Boolean) as string[];

  return `# Validation Runbook

How to check your work in this repo. Agents often guess commands incorrectly — these are the **exact** commands detected from \`package.json\` and the lockfile.

## Commands

| Step | Command |
| --- | --- |
${table}

## Recommended order

\`\`\`bash
${ordered.length ? ordered.join("\n") : "# No scripts detected in package.json"}
\`\`\`

## Notes

- Package manager: **${profile.packageManager}**. ${
    profile.packageManager !== "unknown"
      ? `Do not use other package managers.`
      : `No lockfile detected — confirm the intended package manager before installing.`
  }
${profile.allScripts.length ? `\n### All package.json scripts\n\n${profile.allScripts.map((sc) => `- \`${sc.name}\`: \`${sc.raw}\``).join("\n")}` : ""}
`;
}

