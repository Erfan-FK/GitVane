// ---------------------------------------------------------------------------
// docs/auth-flow.md
// ---------------------------------------------------------------------------
export function buildAuthFlowMd(profile: RepoProfile): string {
  const provider =
    profile.frameworks.find((f) => /next-auth|clerk|auth0|lucia|kinde/i.test(f)) ??
    (profile.database.find((d) => /supabase|firebase/i.test(d)) ?? null);
  const authEnv = profile.envVars.filter((v) => AUTH_ENV_RE.test(v));

  return `# Auth Flow

How authentication works in ${profile.owner}/${profile.repo}, so agents don't bypass it or break protected routes.

## Provider
${provider ? `- Detected auth provider: **${provider}**.` : "- No specific auth library detected — authentication appears custom. Read the middleware and server entry points before changing anything."}
${authEnv.length ? `- Auth-related env vars: ${authEnv.map((v) => `\`${v}\``).join(", ")}.` : ""}
${profile.backendDirs.length ? `- Server code lives in ${profile.backendDirs.map((d) => `\`${d}/\``).join(", ")}.` : ""}

## Rules
- Never remove or weaken authentication or authorization checks.
- Treat session/token handling as security-critical; do not log secrets or tokens.
- New protected routes must enforce the same auth checks as existing ones.
- Read how sessions are created and validated before modifying login, logout, or middleware.
${authEnv.length ? "- Required auth env vars must be set in every environment; never commit real values." : ""}

## Before you finish
- Confirm protected routes still reject unauthenticated requests.
- Confirm sign-in / sign-out still work end to end.
`;
}

