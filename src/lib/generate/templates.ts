// docs/env-vars.md
// ---------------------------------------------------------------------------
export function buildEnvVarsMd(profile: RepoProfile): string {
  const rows = profile.envVars.map((v) => {
    const sensitive = /(SECRET|TOKEN|KEY|PASSWORD|PRIVATE|DSN|URL)/i.test(v);
    const note = sensitive ? "Secret — keep out of version control" : "Configuration";
    return `| \`${v}\` | ${note} |`;
  });

  return `# Environment Variables

Environment variables detected in ${profile.owner}/${profile.repo} (from \`.env.example\` and config). Use these to configure and run the app.

## Variables

| Variable | Notes |
| --- | --- |
${rows.join("\n")}

## Rules
- Copy \`.env.example\` to \`.env.local\` (or your environment) and fill in real values.
- Never commit real secrets. Add new variables to \`.env.example\` with a placeholder.
- Read variables through the existing config layer rather than \`process.env\` scattered across the codebase.
- Treat any variable containing a secret/token/key as sensitive.
`;
}

