// Detection helpers for specialized files
// ---------------------------------------------------------------------------
const AUTH_ENV_RE =
  /(AUTH|CLERK|NEXTAUTH|JWT|SESSION|SUPABASE|OAUTH|COGNITO|FIREBASE|KINDE|AUTH0|MAGIC|TOKEN|SECRET_KEY)/i;

export function hasAuthSignals(profile: RepoProfile): boolean {
  if (profile.database.some((d) => /supabase|firebase/i.test(d))) return true;
  if (profile.frameworks.some((f) => /next-auth|clerk|auth0|lucia|kinde/i.test(f))) return true;
  return profile.envVars.some((v) => AUTH_ENV_RE.test(v));
}

/** Turn a list of directories into a single Copilot `applyTo` glob string. */
function applyToGlob(dirs: string[], fallback: string): string {
  const globs = dirs.length
    ? dirs.map((d) => `${d.replace(/\/$/, "")}/**`)
    : [fallback];
  return globs.join(", ");
}

